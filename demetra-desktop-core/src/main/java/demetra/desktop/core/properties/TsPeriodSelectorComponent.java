    private void updateType(TimeSelector.SelectionType val) {
        switch (val) {
            case All:
            case None:
                from.setEnabled(false);
                to.setEnabled(false);
                first.setEnabled(false);
                last.setEnabled(false);
                break;
            case From:
                from.setEnabled(true);
                to.setEnabled(false);
                first.setEnabled(false);
                last.setEnabled(false);
                break;
            case To:
                from.setEnabled(false);
                to.setEnabled(true);
                first.setEnabled(false);
                last.setEnabled(false);
                break;
            case Between:
                from.setEnabled(true);
                to.setEnabled(true);
                first.setEnabled(false);
                last.setEnabled(false);
                break;
            case Last:
                from.setEnabled(false);
                to.setEnabled(false);
                first.setEnabled(false);
                last.setEnabled(true);
                break;
            case First:
                from.setEnabled(false);
                to.setEnabled(false);
                first.setEnabled(true);
                last.setEnabled(false);
                break;
            case Excluding:
                from.setEnabled(false);
                to.setEnabled(false);
                first.setEnabled(true);
                last.setEnabled(true);
                break;
        }
        fromLabel.setEnabled(from.isEnabled());
        toLabel.setEnabled(to.isEnabled());
        firstLabel.setEnabled(first.isEnabled());
        lastLabel.setEnabled(last.isEnabled());
    }
    
    public TimeSelector getTsPeriodSelector() {
        switch ((TimeSelector.SelectionType) type.getSelectedItem()) {
            case None:
                return TimeSelector.none();
            case From:{
                Date d0=from.getDate();
                return TimeSelector.from(d0 == null ? LocalDateTime.MIN : CalendarUtility.toLocalDate(d0).atStartOfDay());
            }
            case To:{
                Date d1=to.getDate();
                return TimeSelector.to(d1 == null ? LocalDateTime.MAX : CalendarUtility.toLocalDate(d1).atStartOfDay());
            }
            case Between:{
                Date d0=from.getDate();
                Date d1=to.getDate();
                
                return TimeSelector.between(d0 == null ? LocalDateTime.MIN : CalendarUtility.toLocalDate(d0).atStartOfDay(),
                        d1 == null ? LocalDateTime.MAX : CalendarUtility.toLocalDate(d1).atStartOfDay());
            }
            case Last:
                return TimeSelector.last(last.getValue());
            case First:
                return TimeSelector.first(first.getValue());
            case Excluding:
                return TimeSelector.excluding(first.getValue(), last.getValue());
            default:
                return TimeSelector.all();
        }
    }

    public void setTsPeriodSelector(TimeSelector value) {
        type.setSelectedItem(value.getType());
        switch (value.getType()) {
            case All:
            case None:
                from.setDate(null);
                to.setDate(null);
                first.setValue(0);
                last.setValue(0);
                break;
            case From:
                from.setDate(value.getD0()== LocalDateTime.MIN ? null : 
                        CalendarUtility.toDate(value.getD0().toLocalDate()));
                to.setDate(null);
                first.setValue(0);
                last.setValue(0);
                break;
            case To:
                from.setDate(null);
                to.setDate(value.getD1()== LocalDateTime.MAX ? null : 
                        CalendarUtility.toDate(value.getD1().toLocalDate()));
                first.setValue(0);
                last.setValue(0);
                break;
            case Between:
                from.setDate(value.getD0()== LocalDateTime.MIN ? null : 
                        CalendarUtility.toDate(value.getD0().toLocalDate()));
                to.setDate(value.getD1()== LocalDateTime.MAX ? null : 
                        CalendarUtility.toDate(value.getD1().toLocalDate()));
                first.setValue(0);
                last.setValue(0);
                break;
            case Last:
                from.setDate(null);
                to.setDate(null);
                first.setValue(0);
                last.setValue(value.getN1());
                break;
            case First:
                from.setDate(null);
                to.setDate(null);
                first.setValue(value.getN0());
                last.setValue(0);
                break;
            case Excluding:
                from.setDate(null);
                to.setDate(null);
                first.setValue(value.getN0());
                last.setValue(value.getN1());
                break;
        }
    }
