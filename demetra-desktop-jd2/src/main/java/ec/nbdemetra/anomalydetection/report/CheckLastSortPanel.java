    private void fillList() {
        List<ICheckLastReportFactory> factories = CheckLastReportManager.getInstance().getFactories();        
        DefaultComboBoxModel<ICheckLastReportFactory> model = new DefaultComboBoxModel(factories.toArray());
        reportCombo.setModel(model);
    }
    
    public ICheckLastReportFactory getFactory() {
        return (ICheckLastReportFactory)reportCombo.getSelectedItem();
    }

    public AnomalyPojoComparator getComparator() {
        if (ascendingRadio.isSelected()) {
            switch (sortComboBox.getSelectedIndex()) {
                case 1:
                    return AnomalyPojoComparator.Relative_Asc;
                case 2:
                    return AnomalyPojoComparator.Absolute_Asc;
                case 3:
                    return AnomalyPojoComparator.Period_Asc;
                default:
                    return null;
            }
        } else {
            switch (sortComboBox.getSelectedIndex()) {
                case 1:
                    return AnomalyPojoComparator.Relative_Desc;
                case 2:
                    return AnomalyPojoComparator.Absolute_Desc;
                case 3:
                    return AnomalyPojoComparator.Period_Desc;
                default:
                    return null;
            }
        }
    }

